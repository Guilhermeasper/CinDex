package Pacote;

public class Pacote {
    private int codigo;
    private int peso;
    private int altura;
    private int largura;
    private int comprimento;
    Pacote(int codigo, int peso, int altura, int largura, int comprimento){
        this.codigo = codigo;
        this.peso = peso;
        this.altura = altura;
        this.largura = largura;
        this.comprimento = comprimento;
    }

}
